export function localizeProduct(lang, product) {
  if (!product || lang === 'en') return product;
  const pack = PRODUCT_I18N[lang]?.[product.id];
  if (!pack) return product;
  return {
    ...product,
    subtitle: pack.subtitle ?? product.subtitle,
    tagline: pack.tagline ?? product.tagline,
    benefits: pack.benefits ?? product.benefits,
    composition: pack.composition ?? product.composition,
    usage: {
      ...product.usage,
      ...(pack.usage || {})
    }
  };
}
